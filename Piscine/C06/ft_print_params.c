#include	<unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	b;

	i = 1;
	b = 0;
	while (i < argc)
	{
		while (argv[i][b] != '\0')
		{
			write (1, &argv[i][b], 1);
			b++;
		}
		b = 0;
		write (1, "\n", 1);
		i++;
	}
}
